package bean;

public class EducationInfo {

    private String headcount;//总人数
    private String juniorhighschoolsum;//初中人数
    private String seniorsighschoolsum;//高中人数
    private String technicalsecondaryschoolsum;//中专人数
    private String juniorcollegesum;//大学专科人数
    private String bachelordegreesum;//大学本科人数
    private String mastersum;//硕士人数
    private String doctorsum;//博士人数
    private String postdoctorsum;//博士后人数

    public String getHeadcount() {
        return headcount;
    }

    public void setHeadcount(String headcount) {
        this.headcount = headcount;
    }

    public String getJuniorhighschoolsum() {
        return juniorhighschoolsum;
    }

    public void setJuniorhighschoolsum(String juniorhighschoolsum) {
        this.juniorhighschoolsum = juniorhighschoolsum;
    }

    public String getSeniorsighschoolsum() {
        return seniorsighschoolsum;
    }

    public void setSeniorsighschoolsum(String seniorsighschoolsum) {
        this.seniorsighschoolsum = seniorsighschoolsum;
    }

    public String getTechnicalsecondaryschoolsum() {
        return technicalsecondaryschoolsum;
    }

    public void setTechnicalsecondaryschoolsum(String technicalsecondaryschoolsum) {
        this.technicalsecondaryschoolsum = technicalsecondaryschoolsum;
    }

    public String getJuniorcollegesum() {
        return juniorcollegesum;
    }

    public void setJuniorcollegesum(String juniorcollegesum) {
        this.juniorcollegesum = juniorcollegesum;
    }

    public String getBachelordegreesum() {
        return bachelordegreesum;
    }

    public void setBachelordegreesum(String bachelordegreesum) {
        this.bachelordegreesum = bachelordegreesum;
    }

    public String getMastersum() {
        return mastersum;
    }

    public void setMastersum(String mastersum) {
        this.mastersum = mastersum;
    }

    public String getDoctorsum() {
        return doctorsum;
    }

    public void setDoctorsum(String doctorsum) {
        this.doctorsum = doctorsum;
    }

    public String getPostdoctorsum() {
        return postdoctorsum;
    }

    public void setPostdoctorsum(String postdoctorsum) {
        this.postdoctorsum = postdoctorsum;
    }
}
